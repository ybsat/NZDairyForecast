full <- read.csv("fullseries.csv")
mBMP_P <- lm(BMP_Price~., full[-1:-48,c(-1,-2,-6,-7,-8)])
mBMP_Q <- lm(BMP_QtySold~., full[-1:-48,c(-1,-2,-5,-7,-8)])
mBUT_P <- lm(BUT_Price~., full[-1:-65,c(-1,-2,-8)])
mBUT_Q <- lm(BUT_QtySold~., full[-1:-65,c(-1,-2,-7)])            
full$BMP_Price[1:48] = predict(mBMP_P, full[1:48,c(-1,-2,-5,-6,-7,-8)])
full$BMP_QtySold[1:48] = predict(mBMP_Q, full[1:48,c(-1,-2,-5,-6,-7,-8)])
full$BUT_Price[1:65] = predict(mBUT_P, full[1:65,c(-1,-2,-7,-8)])
full$BUT_QtySold[1:65] = predict(mBUT_Q, full[1:65,c(-1,-2,-7,-8)])